public class NumeroActeCelibataire {

	public String idActeCelibataire;

	public long numero;

	public int annee;

	public String getIdActeCelibataire() {
		return idActeCelibataire;
	}

	public void setIdActeCelibataire(String idActeCelibataire) {
		this.idActeCelibataire = idActeCelibataire;
	}

	public long getNumero() {
		return numero;
	}

	public void setNumero(long numero) {
		this.numero = numero;
	}

	public int getAnnee() {
		return annee;
	}

	public void setAnnee(int annee) {
		this.annee = annee;
	}



}
